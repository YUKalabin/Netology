# Квадрат
length_q = int(input("Введите длину стороны квадрата:"))
print("Периметр:" , length_q * 4 )
print("Площадь:" , length_q ** 2)

# Прямоугольник
length_a = int(input("Введите длину прямоугольника:"))
length_b = int(input("Введите ширину прямоугольника:"))
print("Периметр:" , (length_a + length_b) * 2 )
print("Площадь:" , length_a * length_b)